    private void carregarCalendario(){
        UtilDateModel model = new UtilDateModel();
        
        Properties propriedades = new Properties();
        propriedades.put("text.today", "Hoje");
        propriedades.put("text.month", "Mês");
        propriedades.put("text.year", "Ano");
        
        JDatePanelImpl datePanel = new JDatePanelImpl(model, propriedades);
        
        datePicker = new JDatePickerImpl(datePanel, new DateLabelFormatter());
        
        painelDataCompra.setLayout(new java.awt.BorderLayout());
        
        painelDataCompra.add(datePicker, java.awt.BorderLayout.CENTER);
        
        painelDataCompra.revalidate();
        painelDataCompra.repaint();
        
    }
    
    public void definirDataAtual(){
        LocalDate hoje = LocalDate.now();
        
        datePicker.getModel().setDate(hoje.getYear(), hoje.getMonthValue() -1, hoje.getDayOfMonth());
        
        datePicker.getModel().setSelected(true);
    }
    
    private void CarregarComboInsumos(){
        try {
            List<Insumo> lista = service.MostrarInsumoComboBx();
            jcbxInsumo.removeAllItems();
            
            for(Insumo insumo : lista){
                jcbxInsumo.addItem(insumo);
            }
        } catch (Exception erro) {
            erro.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro para carregar insumo no comboBox" + erro);
        }
    }
    
    private void CarregarTabelaItens(){
            DefaultTableModel modelo = (DefaultTableModel) jTableItems.getModel();
            modelo.setRowCount(0);
            
            double valorTotalCompra = 0;
            
            for(ItemCompra item : itensCompra){
                double valorTotalItem = item.getQtd() * item.getValorUnitario();
                
                modelo.addRow(new Object[]{
                    item.getInsumo().getDescricao(),
                    item.getInsumo().getUnidademedida(),
                    item.getQtd(),
                    item.getValorUnitario(),
                    valorTotalItem
                });
                valorTotalCompra += valorTotalItem;
            }
            txtValorTotal.setText(String.valueOf(valorTotalCompra));
    }
    
    private void CarreggarComboFormaPagamento(){
        jComboBox1.removeAllItems();
        
        for(FormaPagamento pagamento : FormaPagamento.values()){
            jComboBox1.addItem(pagamento);
        }
    }

