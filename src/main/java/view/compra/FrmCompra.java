    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
       try {
            Insumo insumoSelecionado = (Insumo) jcbxInsumo.getSelectedItem();

            double quantidade = Double.parseDouble(txtQtd.getText());
            double valorTotalItem = Double.parseDouble(txtValorPago.getText().replace(",", "."));
            
            double valorUnitario = valorTotalItem / quantidade;

            ItemCompra item = new ItemCompra();
            item.setInsumo(insumoSelecionado);
            item.setQtd(quantidade);
            item.setValorUnitario(valorUnitario);
            item.setValorTotalItem(valorTotalItem);

            itensCompra.add(item);
            CarregarTabelaItens();
        } catch (Exception erro) {
            erro.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao adicionar item - Erro:" + erro);
        }
        
    }//GEN-LAST:event_btnAdicionarItemActionPerformed

    private void btnSalvarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarCompraActionPerformed
        try {
            Object valorSelecionado = datePicker.getModel().getValue();
            
            if(valorSelecionado == null  || itensCompra.isEmpty() || txtQtd.getText().isEmpty() || txtValorPago.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Certifique-se que todos os campos estão preenchidos!");
                return;
            }
            
            java.util.Date dataSelecionada = (java.util.Date) valorSelecionado;
            
            LocalDate dataCompra = dataSelecionada.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            double valotTotal = Double.parseDouble(txtValorTotal.getText().replace(",", "."));
            compra.setDataCompra(dataCompra);
            compra.setValorTotal(valotTotal);
            compra.setItens(itensCompra);
            
            service.registrarCompra(compra);
            JOptionPane.showMessageDialog(this, "Compra registrada com sucesso!");   
            limpar.LimparCampos(jPanel2);
            itensCompra.clear();
            DefaultTableModel modelo =  (DefaultTableModel) jTableItems.getModel();
            modelo.setRowCount(0);
            txtValorTotal.setText("");
        } catch (Exception erro) {
            erro.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro para regitsrar a compra - Erro:" + erro.getMessage());
        }
    }//GEN-LAST:event_btnSalvarCompraActionPerformed

    private void btnRemoverItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverItemActionPerformed
        int linhaSelecionada = jTableItems.getSelectedRow();
        
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione algum item da tabela para remover");
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(this, "Deseja remover o item selecionado?", "Confirmar remoção do item", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            itensCompra.remove(linhaSelecionada);
            CarregarTabelaItens();
        }
    }//GEN-LAST:event_btnRemoverItemActionPerformed

